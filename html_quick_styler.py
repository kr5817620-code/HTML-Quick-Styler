def use_template(name):

    templates = {
        "Portfolio Website":
        "Hero, Portfolio Gallery, About, Contact, Footer",

        "SaaS Landing Page":
        "Hero, Features, Pricing, Testimonials, CTA, Footer",

        "Company Website":
        "Header, About, Services, Team, Contact, Footer"
    }

    return templates.get(name)


