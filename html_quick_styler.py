def show_palette(style):

    palettes = {
        "modern": ["#6366f1","#7c3aed","#0f172a","#f1f5f9"],
        "vibrant": ["#f97316","#ef4444","#1e293b","#f9fafb"],
        "ocean": ["#0284c7","#0ea5e9","#0f172a","#e0f2fe"],
        "forest": ["#16a34a","#4ade80","#052e16","#ecfdf5"],
        "sunset": ["#f97316","#fb7185","#431407","#fff7ed"],
        "luxury": ["#c9a227","#e5e4e2","#111","#fff"],
        "creative": ["#8b5cf6","#ec4899","#1e1b4b","#faf5ff"],
        "minimal": ["#000","#6b7280","#fff","#111"]
    }

    colors = palettes.get(style)

    html = "<h3>Palette for "+style+"</h3>"

    for c in colors:
        html += f"""
        <div style="background:{c};height:50px;margin:10px;border-radius:6px;"></div>
        """

    return html


