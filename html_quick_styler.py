with gr.Blocks() as app:

    gr.Markdown("# 🧠 HTML Quick-Styler Advanced")
    gr.Markdown("Professional HTML Page Generator with Auto-Styling")

    with gr.Tabs():

        # PAGE 1
        with gr.Tab("Generate Page"):

            with gr.Row():

                with gr.Column():

                    gr.Markdown("### Page Details")

                    title = gr.Textbox(value="Modern Portfolio Website")

                    desc = gr.Textbox(
                        lines=4,
                        value="Hero section with welcome message, features list with 3 items"
                    )

                    style = gr.Dropdown(
                        ["modern","vibrant","ocean","forest","sunset","luxury","creative","minimal"],
                        value="modern"
                    )

                    btn = gr.Button("Generate HTML")

                with gr.Column():

                    gr.Markdown("### Output")

                    code = gr.Code(language="html")

        # PAGE 2
        with gr.Tab("Live Preview"):

            preview_btn = gr.Button("Load Preview")

            preview = gr.HTML()

        # PAGE 3
        with gr.Tab("Color Palettes"):

            palette_style = gr.Radio(
                ["modern","vibrant","ocean","forest","sunset","luxury","creative","minimal"],
                value="modern"
            )

            palette_btn = gr.Button("Get Palette")

            palette_display = gr.HTML()

        # PAGE 4
        with gr.Tab("Templates"):

            template = gr.Radio(
                ["Portfolio Website","SaaS Landing Page","Company Website"]
            )

            template_btn = gr.Button("Use Template")

            template_output = gr.Textbox()

        # PAGE 5
        with gr.Tab("Guide"):

            guide_btn = gr.Button("Show Guide")

            guide = gr.HTML()

    # FUNCTIONS

    btn.click(generate_html,[title,desc,style],[code,preview])

